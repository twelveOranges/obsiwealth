/** Lookup helper that throws at module-init if the icon isn't registered. */
function moneyIconByName(name: string): string {
  const found = getCategory("2d", "money")!.icons.find((i) => i.name === name);
  if (!found) {
    throw new Error(`[iconLibrary] money icon "${name}" not registered`);
  }
  return found.id;
}

