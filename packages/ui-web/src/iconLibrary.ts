/** Flat list across every dimension & category — used by `findIcon`. */
export const ALL_ICONS: IconDefinition[] = (() => {
  const out: IconDefinition[] = [];
  ICON_LIBRARY.forEach((dim) => {
    dim.categories.forEach((cat) => {
      cat.icons.forEach((icon) => out.push(icon));
    });
  });
  return out;
})();

/**
 * Look up an entire category by id inside a specific dimension. Useful for
 * the `variant="money"` branch of the picker which only wants the "money"
 * group from 2D.
 */
export function getCategory(
  dimension: IconDimension,
  categoryId: IconCategoryId,
): IconCategory | undefined {
  const dim = ICON_LIBRARY.find((d) => d.dimension === dimension);
  return dim?.categories.find((c) => c.id === categoryId);
}

