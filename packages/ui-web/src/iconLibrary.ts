/**
 * Turn raw SVG markup into a data URL so that all of our existing rendering
 * paths (`<img src={icon.src}>`, `getIconPath` passthrough, stored `fund.icon`
 * strings, etc.) work without any branching between "file-backed" and
 * "inline" icons.
 */
function svgDataUrl(svg: string): string {
  const trimmed = svg.replace(/\s+/g, " ").trim();
  return `data:image/svg+xml;utf8,${encodeURIComponent(trimmed)}`;
}

function entry(id: string, name: string): IconDefinition {
  return { id, name, src: id };
}

